package com.sapient.cui;

class List2 {
// Compute the area of a circle.
  public static void main(String[] args) {
    double pi; 
    double r; 
    double a; 

    r = 10.8; // radius of circle
    pi = 3.1416; // pi, approximately
    a = pi * r * r; // compute area

    System.out.println("Area of circle is " + a);
  }
}


